public class holyDayInfo {

    private String empno, fname, restname, type, period, startat, endat, summry, realperiod, rest, status;

    public void setEmpno(String empno) {
        this.empno = empno;
    }

    public String getEmpno() {
        return empno;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getFname() {
        return fname;
    }

    public void setRestname(String restname) {
        this.restname = restname;
    }

    public String getRestname() {
        return restname;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getPeriod() {
        return period;
    }

    public void setStartat(String startat) {
        this.startat = startat;
    }

    public String getStartat() {
        return startat;
    }

    public void setEndat(String endat) {
        this.endat = endat;
    }

    public String getEndat() {
        return endat;
    }

    public void setSummry(String summry) {
        this.summry = summry;
    }

    public String getSummry() {
        return summry;
    }

    public void setRealperiod(String realperiod) {
        this.realperiod = realperiod;
    }

    public String getRealperiod() {
        return realperiod;
    }

    public void setRest(String rest) {
        this.rest = rest;
    }

    public String getRest() {
        return rest;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatus() {
        return status;
    }
}
